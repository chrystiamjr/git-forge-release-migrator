import 'package:gfrm_dart/src/models/runtime_options.dart';

RuntimeOptions buildGuiRuntimeOptions({
  required String commandName,
  required String sourceProvider,
  required String sourceUrl,
  required String sourceToken,
  required String targetProvider,
  required String targetUrl,
  required String targetToken,
  required String settingsProfile,
  required String fromTag,
  required String toTag,
  required bool skipTagMigration,
  required bool dryRun,
  required String workdir,
  required String logFile,
  required int downloadWorkers,
  required int releaseWorkers,
  required String checkpointFile,
  required String tagsFile,
}) {
  return RuntimeOptions(
    commandName: commandName,
    sourceProvider: sourceProvider,
    sourceUrl: sourceUrl,
    sourceToken: sourceToken,
    targetProvider: targetProvider,
    targetUrl: targetUrl,
    targetToken: targetToken,
    migrationOrder: '$sourceProvider-to-$targetProvider',
    skipTagMigration: skipTagMigration,
    fromTag: fromTag,
    toTag: toTag,
    dryRun: dryRun,
    nonInteractive: true,
    workdir: workdir,
    logFile: logFile,
    loadSession: false,
    saveSession: false,
    resumeSession: false,
    sessionFile: '',
    sessionTokenMode: 'env',
    sessionSourceTokenEnv: defaultSourceTokenEnv,
    sessionTargetTokenEnv: defaultTargetTokenEnv,
    settingsProfile: settingsProfile,
    downloadWorkers: downloadWorkers,
    releaseWorkers: releaseWorkers,
    checkpointFile: checkpointFile,
    tagsFile: tagsFile,
    noBanner: true,
    quiet: true,
    jsonOutput: false,
    progressBar: false,
    demoMode: false,
    demoReleases: 5,
    demoSleepSeconds: 1.0,
  );
}
